package cn.unis.transit;

/**
 * 用户 注册的 游乐场列表 的单个bean
 * @author Administrator
 *
 */
public class UserRegisteredArcade {
	
	private String game_center_key;
	private String game_center_name;
	
	
	
	public String getGame_center_key() {
		return game_center_key;
	}
	public void setGame_center_key(String game_center_key) {
		this.game_center_key = game_center_key;
	}
	public String getGame_center_name() {
		return game_center_name;
	}
	public void setGame_center_name(String game_center_name) {
		this.game_center_name = game_center_name;
	}
		
	
}
